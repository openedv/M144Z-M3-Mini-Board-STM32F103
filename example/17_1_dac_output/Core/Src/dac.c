/**
* @brief  	Sets the channel 1/2 output voltage
* @param 	outx: 1, channel 1; Two, channel two
* @param 	vol: 0 to 3300, indicating 0 to 3.3V
* @retval 	None
*/
void dac_set_voltage(uint8_t outx, uint16_t vol)
{
    double temp = vol;
    temp /= 1000;
    temp = temp * 4096 / 3.3;

    if (temp >= 4096)temp = 4095;   /* If the value is greater than or equal to 4096, then it will be capped at 4095 */

    if (outx == 1)   				/* Channel 1 */
    {
        HAL_DAC_SetValue(&hdac, DAC_CHANNEL_1, DAC_ALIGN_12B_R, temp); /* Format the DAC value in a 12-digit right alignment data format. */
    }
    else            				/* Channel 2 */
    {
        HAL_DAC_SetValue(&hdac, DAC_CHANNEL_2, DAC_ALIGN_12B_R, temp); /* Set the DAC value in a 12-bit right-aligned data format. */
    }
}

